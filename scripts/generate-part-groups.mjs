// STAGE 5 of the parts pipeline: cross-reference cluster pages.
// One page per cross-ref group from CROSS_REFERENCE_GROUPS.csv — all the
// numbers industry data references together for one brand/model/part-type,
// with "does your part carry any of these numbers?" framing. No per-number
// interchange claims; the sales team confirms before quoting.
//
// Usage: node scripts/generate-part-groups.mjs <CROSS_REFERENCE_GROUPS.csv>
import fs from 'node:fs';
import path from 'node:path';

const csvPath = process.argv[2];
if (!csvPath) {
  console.error('Usage: node scripts/generate-part-groups.mjs <CROSS_REFERENCE_GROUPS.csv>');
  process.exit(1);
}

function parseCsv(text) {
  const rows = [];
  let row = [], field = '', q = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (q) {
      if (c === '"' && text[i + 1] === '"') { field += '"'; i++; }
      else if (c === '"') q = false;
      else field += c;
    } else if (c === '"') q = true;
    else if (c === ',') { row.push(field); field = ''; }
    else if (c === '\n' || c === '\r') {
      if (field !== '' || row.length) { row.push(field); rows.push(row); row = []; field = ''; }
      if (c === '\r' && text[i + 1] === '\n') i++;
    } else field += c;
  }
  if (field !== '' || row.length) { row.push(field); rows.push(row); }
  return rows;
}
const slugify = (s) => s.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '');
const clamp = (s, max) => (s.length <= max ? s : s.slice(0, max - 1).trimEnd() + '…');
const esc = (s) => String(s).replace(/'/g, "''");
const titleCase = (s) =>
  s.toLowerCase().replace(/\b[a-z]/g, (c) => c.toUpperCase()).replace(/\bO Ring\b/g, 'O-Ring');

const g = parseCsv(fs.readFileSync(csvPath, 'utf8'));
const h = g.shift().map((c) => c.trim());
const ix = Object.fromEntries(h.map((c, i) => [c, i]));

// Existing individual part pages, for linking group members
const partsDir = path.join('src', 'content', 'parts');
const pageSlugs = new Set(
  fs.readdirSync(partsDir).filter((f) => f.endsWith('.md')).map((f) => f.replace(/\.md$/, '')),
);

const outDir = path.join('src', 'content', 'part-groups');
fs.mkdirSync(outDir, { recursive: true });

// First pass: detect slug collisions (same model+partType from different groups)
const slugCount = new Map();
const parsed = [];
for (const r of g) {
  const get = (c) => (r[ix[c]] ?? '').trim();
  const groupId = get('cross_ref_group_id');
  const model = get('model_key');
  const desc = get('description_key');
  const numbers = get('part_numbers').split(';').map((s) => s.trim()).filter(Boolean);
  if (!groupId || !model || !desc || numbers.length < 2) continue;
  if (/other|unknown/i.test(model)) continue; // meaningless model = no page topic
  const brand = (get('brands').split(/[;,]/)[0] ?? '').trim() || 'Crusher';
  const baseSlug = slugify(`${brand}-${model}-${desc}`);
  slugCount.set(baseSlug, (slugCount.get(baseSlug) ?? 0) + 1);
  parsed.push({ groupId, model, desc, numbers, brand, baseSlug });
}

let written = 0;
const seen = new Map();
const usedTitles = new Set();
for (const p of parsed) {
  const dup = slugCount.get(p.baseSlug) > 1;
  const nth = (seen.get(p.baseSlug) ?? 0) + 1;
  seen.set(p.baseSlug, nth);
  const slug = dup ? `${p.baseSlug}-${nth}` : p.baseSlug;
  const partType = titleCase(p.desc);
  const setLabel = dup ? ` (Set ${nth})` : '';

  const body = p.numbers
    .map((pn) => {
      const pslug = slugify(`${p.brand}-${pn}`);
      return pageSlugs.has(pslug) ? `- [${pn}](/parts/${pslug}/)` : `- ${pn}`;
    })
    .join('\n');

  // Clamping can make distinct titles collide — disambiguate with the slug
  // ordinal so every page keeps a unique title.
  let metaTitle = clamp(`${p.brand} ${p.model} ${partType}${setLabel} Numbers | HardRok`, 60);
  for (let k = 2; usedTitles.has(metaTitle); k++) {
    metaTitle = clamp(`${p.brand} ${p.model} ${partType} ${k} | HardRok`, 60);
  }
  usedTitles.add(metaTitle);

  const fm = [
    `# Generated by scripts/generate-part-groups.mjs (group ${p.groupId})`,
    `brand: '${esc(p.brand)}'`,
    `modelFamily: '${esc(p.model)}'`,
    `partType: '${esc(partType + setLabel)}'`,
    `groupId: '${esc(p.groupId)}'`,
    `numberCount: ${p.numbers.length}`,
    `metaTitle: '${esc(metaTitle)}'`,
    `metaDescription: '${esc(clamp(`${p.numbers.length} cross-referenced ${p.brand} ${p.model} ${partType.toLowerCase()} part numbers. Have one of these numbers? HardRok can supply it — call (866) 427-3765.`, 155))}'`,
    `draft: false`,
  ];
  fs.writeFileSync(path.join(outDir, `${slug}.md`), `---\n${fm.join('\n')}\n---\n\n${body}\n`);
  written++;
}

console.log(`part-group pages written: ${written} (of ${parsed.length} qualifying groups)`);
