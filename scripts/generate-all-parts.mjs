// FINAL STAGE of the parts pipeline: enforce the uniform rule — EVERY
// unique (brand, part number) in the master dataset gets exactly one page.
// Existing pages (official/dealer/cross-ref/reference) are left untouched;
// this fills every remaining number with a reference page ("call to
// confirm") whose copy adapts to whatever data the sightings carry.
// Skips brand 'unknown' (nothing sensible to put in a title).
//
// Usage: node scripts/generate-all-parts.mjs <normalized.csv>
import fs from 'node:fs';
import path from 'node:path';

const csvPath = process.argv[2];
if (!csvPath) {
  console.error('Usage: node scripts/generate-all-parts.mjs <normalized.csv>');
  process.exit(1);
}

function parseCsv(text) {
  const rows = [];
  let row = [], field = '', q = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (q) {
      if (c === '"' && text[i + 1] === '"') { field += '"'; i++; }
      else if (c === '"') q = false;
      else field += c;
    } else if (c === '"') q = true;
    else if (c === ',') { row.push(field); field = ''; }
    else if (c === '\n' || c === '\r') {
      if (field !== '' || row.length) { row.push(field); rows.push(row); row = []; field = ''; }
      if (c === '\r' && text[i + 1] === '\n') i++;
    } else field += c;
  }
  if (field !== '' || row.length) { row.push(field); rows.push(row); }
  return rows;
}
const slugify = (s) => s.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '');
const clamp = (s, max) => (s.length <= max ? s : s.slice(0, max - 1).trimEnd() + '…');
const esc = (s) => String(s).replace(/'/g, "''");

const m = parseCsv(fs.readFileSync(csvPath, 'utf8'));
const h = m.shift().map((c) => c.trim());
const ix = Object.fromEntries(h.map((c, i) => [c, i]));

const partsDir = path.join('src', 'content', 'parts');
const existing = new Set(fs.readdirSync(partsDir).filter((f) => f.endsWith('.md')).map((f) => f.replace(/\.md$/, '')));

// Group sightings by (brand, pn) slug
const groups = new Map();
for (const r of m) {
  const get = (c) => (r[ix[c]] ?? '').trim();
  const pn = get('part_number');
  const brand = get('canonical_brand') || get('brand_family') || 'unknown';
  if (!pn || brand === 'unknown') continue;
  const slug = slugify(`${brand}-${pn}`);
  if (existing.has(slug)) continue;
  if (!groups.has(slug)) groups.set(slug, { pn, brand, models: new Set(), sources: new Set() });
  const g = groups.get(slug);
  const model = get('model_family_extracted');
  if (model && !/other|unknown/i.test(model)) g.models.add(model);
  const url = get('source_url');
  try {
    g.sources.add(new URL(url).hostname);
  } catch {
    if (get('supplier_or_source')) g.sources.add(get('supplier_or_source'));
  }
}

const usedTitles = new Set();
let written = 0;
for (const [slug, g] of groups) {
  const model = [...g.models][0] ?? '';
  const nSources = g.sources.size;
  let metaTitle = clamp(`${g.brand} ${g.pn} | Part Quote | HardRok`, 60);
  for (let k = 2; usedTitles.has(metaTitle); k++) metaTitle = clamp(`${g.brand} ${g.pn} (${k}) | HardRok`, 60);
  usedTitles.add(metaTitle);

  const srcNote =
    nSources >= 2
      ? `This number is recorded consistently across ${nSources} independent industry catalog sources${model ? ` in connection with ${g.brand} ${model} equipment` : ''}.`
      : `This number appears in industry catalog data${model ? ` in connection with ${g.brand} ${model} equipment` : ''}.`;

  const fm = [
    `# Generated by scripts/generate-all-parts.mjs (uniform coverage: one number, one page)`,
    `oemNumber: '${esc(g.pn)}'`,
    `brand: '${esc(g.brand)}'`,
    `pageType: 'reference'`,
    `metaTitle: '${esc(metaTitle)}'`,
    `metaDescription: '${esc(clamp(`${g.brand} part number ${g.pn} — call HardRok Equipment at (866) 427-3765 to speak with a representative for pricing, availability, and fitment.`, 155))}'`,
    `draft: false`,
  ];
  const body = `${srcNote} Full details for this part number require confirmation from our sales team — send it through the form or call and read it to a parts specialist.`;
  fs.writeFileSync(path.join(partsDir, `${slug}.md`), `---\n${fm.join('\n')}\n---\n\n${body}\n`);
  written++;
}

console.log(`new pages: ${written}; total part pages: ${existing.size + written}`);
